package com.sep.billardapp.example.billard_app_02;

/***
 * Class for the billard balls. Has the x and y coordinates of center as well as radius as attributes.
 */
public class Ball {

    double x, y, r;

    public Ball(double x, double y, double r) {
        this.x = x;
        this.y = y;
        this.r = r;
    }
}
